/// Quick animation validity lookup
static struct AnimationLookup
{
	FlatHashSet<Impl::String> valid;

	AnimationLookup()
	{
		for (int i = 1 /* skip animation 0 since it's invalid */; i < GLM_COUNTOF(AnimationNames); ++i)
		{
			valid.emplace(Impl::String(AnimationNames[i]));
		}
	}
} AnimationNamesLookup;

/// Quick animation validity lookup
static struct AnimationLibraryLookup
{
	FlatHashSet<Impl::String> valid;

	AnimationLibraryLookup()
	{
		for (int i = 0; i < GLM_COUNTOF(AnimLibs); ++i)
		{
			valid.emplace(Impl::String(AnimLibs[i]));
		}
	}
} AnimationLibraryLookup;

inline Pair<StringView, StringView> splitAnimationNames(int ID)
{
	if (ID <= 0 || ID >= GLM_COUNTOF(AnimationNames))
	{
		return { "", "" };
	}

	StringView full = AnimationNames[ID];
	unsigned int idx = full.find(':');
	if (idx == -1)
	{
		return { "", "" };
	}

	return { full.substr(0, idx), full.substr(idx + 1) };
}

inline bool animationNameValid(StringView lib, StringView name)
{
	Impl::String fullName(lib);
	fullName += ':' + Impl::String(name);
	std::transform(fullName.begin(), fullName.end(), fullName.begin(), [](unsigned char c)
		{
			return std::toupper(c);
		});
	return AnimationNamesLookup.valid.find(fullName) != AnimationNamesLookup.valid.end();
}

inline bool animationLibraryValid(StringView lib, bool v1_0 = true)
{
	Impl::String fullName(lib);
	std::transform(fullName.begin(), fullName.end(), fullName.begin(), [](unsigned char c)
		{
			return std::toupper(c);
		});
	if (AnimationLibraryLookup.valid.find(fullName) != AnimationLibraryLookup.valid.end())
	{
		return true;
	}
	if (v1_0)
	{
		// Check three more libraries, removed in version 1.1
		return fullName == "BLOWJOBS" || fullName == "SEX" || fullName == "SNM";
	}
	return false;
}

/* Interfaces, to be passed around */

/// Holds data to pass when applying an animation to a player
struct AnimationData
{
	float delta; ///< The speed to play the animation
	bool loop; ///< If set to 1, the animation will loop. If set to 0, the animation will play once
	bool lockX; ///< If set to 0, the player is returned to their old X coordinate once the animation is complete (for animations that move the player such as walking). 1 will not return them to their old position
	bool lockY; ///< Same as above but for the Y axis. Should be kept the same as the previous parameter
	bool freeze; ///< Setting this to 1 will freeze the player at the end of the animation. 0 will not
	uint32_t time; ///< Timer in milliseconds. For a never-ending loop it should be 0
	HybridString<16> lib; ///< The animation library of the animation to apply
	HybridString<24> name; ///< The name of the animation to apply

	AnimationData()
		: delta(4.1f)
		, loop(false)
		, lockX(false)
		, lockY(false)
		, freeze(false)
		, time(0)
	{
	}

	/// Constructor
	AnimationData(float delta, bool loop, bool lockX, bool lockY, bool freeze, uint32_t time, StringView lib, StringView name)
		: delta(delta)
		, loop(loop)
		, lockX(lockX)
		, lockY(lockY)
		, freeze(freeze)
		, time(time)
		, lib(lib)
		, name(name)
	{
	}
};
